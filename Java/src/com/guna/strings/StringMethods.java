package com.guna.strings;

/**
 * concat - returns string
 * charAt(takes int) - returns char
 * length() - returns int
 * trim() - returns String
 * split(" ") - returns String
 * toLowerCase - returns String
 * toUpperCase
 * indexOf(takes String, char) - returns int
 * startsWith("")- returns boolean
 * isEmpty()  and isBlank()- returns true if the String is empty
 */
public class StringMethods {
    public static void main(String[] args) {
        String str = "  ";
        System.out.println(str.isBlank()); // isEmpty()
        System.out.println(str.isEmpty());

    }



}
